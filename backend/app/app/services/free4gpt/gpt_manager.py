def clean_text(text: str) -> str:
    # удалить мусорные строки с >
    text = text.split('\n>')[0]

    # удалить [1], [23] и т.п.
    text = re.sub(r'\[\d+\]', '', text)

    # удалить URL
    text = re.sub(r'https?:\/\/[^\s)]+', '', text)

    # удалить пустые строки в конце
    text = re.sub(r'(\n\s*)+$', '', text)

    return text

class GPTManager:
    def __init__(
        self
    ) -> None:
        self.url = "http://free-gpt4:5500/"

    def get_answer(self, question: str) -> str:
        text_question = promt.format(question)

        response = requests.get(
            self.url,
            params={"text": text_question},
            timeout=150
        )
        response.raise_for_status()
        cleaned_text = clean_text(response.text)
        logging.info(cleaned_text)

        if "Login to continue" in response.text:
            requests.get(f"{self.url}/login", timeout=30)

            response = requests.get(
                self.url,
                params={"text": text_question},
                timeout=150
            )
            response.raise_for_status()
            cleaned_text = clean_text(response.text)
            logging.info(cleaned_text)

        return  cleaned_text

